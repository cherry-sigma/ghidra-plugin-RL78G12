  private def ensureRegisterData(program: Program, addrValue: Long, names: Seq[String]): Unit =
    val listing = program.getListing
    val a = addr(program, addrValue)
    if listing.getInstructionAt(a) != null then return

    val hasHighLowAliases = names.exists(_.endsWith("L")) || names.exists(_.endsWith("H"))
    val preferWord = names.exists(looksLikeWordRegister) || hasHighLowAliases

    if listing.getDataAt(a) == null then
      try
        if preferWord then listing.createData(a, WordDataType.dataType)
        else listing.createData(a, ByteDataType.dataType)
      catch
        case _: Exception => ()

  private val tempVarRe =
    """^(?:local(?:_res)?_[0-9A-Fa-f]+|(?:param|temp)_[0-9A-Fa-f]+|(?:b|s|u|i|l|ll|f|d|c|a|p|pc|pu)(?:Var|Stack)?[A-Za-z0-9_]*\d+)$""".r

  override protected def programActivated(program: Program): Unit =
    if !done then
      try
        setupMemoryMap(program)
        annotateKnownRegions(program)
        annotateRegisters(program)
        renameInterrupts(program)
        renameDecompilerTemps(program)
        done = true
      catch
        case e: Exception =>
          done = false
          Msg.showError(this, null, "RL78G12", s"Plugin failed: ${e.getMessage}", e)

  private def addr(program: Program, x: Long): Address =
    program.getAddressFactory.getDefaultAddressSpace.getAddress(x)

  private def annotateRegisters(program: Program): Unit =
    val tx = program.startTransaction("Annotate RL78/G12 registers")
    try
      val grouped = registers.groupBy(_._1).toSeq.sortBy(_._1)

      for (regAddr, entries) <- grouped do
        val names = entries.map(_._2).distinct
        if names.nonEmpty then
          ensureRegisterData(program, regAddr, names)

          val primary = names.head
          createLabelAlias(program, regAddr, primary, makePrimary = true)

          for alias <- names.tail do
            createLabelAlias(program, regAddr, alias, makePrimary = false)

          val aliasText =
            if names.tail.nonEmpty then s"Aliases: ${names.tail.mkString(", ")}"
            else "No aliases"

          val widthText =
            if names.exists(_.endsWith("L")) || names.exists(_.endsWith("H")) || names.exists(looksLikeWordRegister) then
              "Register width: likely 16-bit or mixed 8/16-bit view"
            else
              "Register width: likely 8-bit"

          setPreComment(program, regAddr, s"SFR/SFR2 register: $primary")
          setEolComment(program, regAddr, s"$aliasText; $widthText")

      Msg.showInfo(this, null, "RL78G12", s"Annotated ${grouped.size} register addresses.")
    catch
      case e: Exception =>
        Msg.showError(this, null, "RL78G12", s"Failed to annotate registers: ${e.getMessage}", e)
    finally
      program.endTransaction(tx, true)

  private def createOrRenameLabel(program: Program, addrValue: Long, name: String): Unit =
    val st = program.getSymbolTable
    val a = addr(program, addrValue)

    val primary = st.getPrimarySymbol(a)
    if primary == null then
      st.createLabel(a, name, SourceType.ANALYSIS)
    else
      try
        primary.setName(name, SourceType.ANALYSIS)
      catch
        case _: Exception =>
          try
            st.createLabel(a, s"${name}_${addrValue.toHexString.toUpperCase}", SourceType.ANALYSIS)
          catch
            case _: Exception => ()

  private def appendComment(existing: String, extra: String): String =
    if existing == null || existing.isBlank then extra
    else if existing.contains(extra) then existing
    else existing + "\n" + extra

  private def setPreComment(program: Program, addrValue: Long, text: String): Unit =
    val listing = program.getListing
    val cu = listing.getCodeUnitAt(addr(program, addrValue))
    if cu != null then
      cu.setComment(CodeUnit.PRE_COMMENT, appendComment(cu.getComment(CodeUnit.PRE_COMMENT), text))

  private def setEolComment(program: Program, addrValue: Long, text: String): Unit =
    val listing = program.getListing
    val cu = listing.getCodeUnitAt(addr(program, addrValue))
    if cu != null then
      cu.setComment(CodeUnit.EOL_COMMENT, appendComment(cu.getComment(CodeUnit.EOL_COMMENT), text))

  private def ensureByteData(program: Program, addrValue: Long): Unit =
    val listing = program.getListing
    val a = addr(program, addrValue)
    if listing.getDataAt(a) == null && listing.getInstructionAt(a) == null then
      try
        listing.createData(a, ByteDataType.dataType)
      catch
        case _: Exception => ()

  private def ensureWordData(program: Program, addrValue: Long): Unit =
    val listing = program.getListing
    val a = addr(program, addrValue)
    if listing.getDataAt(a) == null && listing.getInstructionAt(a) == null then
      try
        listing.createData(a, WordDataType.dataType)
      catch
        case _: Exception => ()

  private def ensureRegionLabel(program: Program, addrValue: Long, name: String, comment: String): Unit =
    createOrRenameLabel(program, addrValue, name)
    setPreComment(program, addrValue, comment)

  private def ensureFunction(program: Program, entry: Address, name: String): Unit =
    val fm = program.getFunctionManager
    var fn = fm.getFunctionAt(entry)

    if fn == null then
      val cmd = new CreateFunctionCmd(name, entry, null, SourceType.ANALYSIS)
      cmd.applyTo(program)

    fn = fm.getFunctionAt(entry)
    if fn != null then
      try
        fn.setName(name, SourceType.ANALYSIS)
      catch
        case _: Exception => ()

  private def createUninitializedBlockIfMissing(
    program: Program,
    name: String,
    start: Long,
    end: Long,
    read: Boolean,
    write: Boolean,
    execute: Boolean,
    isVolatile: Boolean = false
  ): Unit =
    val mem = program.getMemory
    val startAddr = addr(program, start)

    if mem.getBlock(startAddr) == null then
      val size = end - start + 1
      val block = mem.createUninitializedBlock(name, startAddr, size, false)
      block.setRead(read)
      block.setWrite(write)
      block.setExecute(execute)
      block.setVolatile(isVolatile)
      Msg.info(this, f"Created block $name @ 0x$start%05X-0x$end%05X")
    else
      Msg.info(this, f"Block already exists for $name @ 0x$start%05X")

  private def setupMemoryMap(program: Program): Unit =
    val tx = program.startTransaction("Setup RL78/G12 memory map")
    try
      // Keep imported ROM if it already exists at 0x00000.
      if program.getMemory.getBlock(addr(program, 0x00000)) == null then
        createUninitializedBlockIfMissing(program, "CODE",       0x00000, 0x007FF, read = true,  write = false, execute = true)

      createUninitializedBlockIfMissing(program, "SFR2",       0xF0000, 0xF07FF, read = true,  write = true,  execute = false, isVolatile = true)
      createUninitializedBlockIfMissing(program, "DATA_FLASH", 0xF1000, 0xF17FF, read = true,  write = true,  execute = false)
      createUninitializedBlockIfMissing(program, "RAM",        0xFFE00, 0xFFEFF, read = true,  write = true,  execute = false)
      createUninitializedBlockIfMissing(program, "SFR",        0xFFF00, 0xFFFFF, read = true,  write = true,  execute = false, isVolatile = true)

      Msg.showInfo(this, null, "RL78G12", "Memory map setup complete.")
    catch
      case e: Exception =>
        Msg.showError(this, null, "RL78G12", s"Failed to setup memory map: ${e.getMessage}", e)
    finally
      program.endTransaction(tx, true)

  private def annotateVectorEntries(program: Program): Unit =
    val mem = program.getMemory

    for ((vec, name) <- vectors) do
      ensureWordData(program, vec.toLong)
      createOrRenameLabel(program, vec.toLong, s"${name}_ADDR")
      setPreComment(program, vec.toLong, s"Interrupt vector entry for $name")

      val vecAddr = addr(program, vec.toLong)
      if mem.contains(vecAddr) && mem.contains(vecAddr.add(1)) then
        val lo = mem.getByte(vecAddr) & 0xff
        val hi = mem.getByte(vecAddr.add(1)) & 0xff
        val target = (hi << 8) | lo

        if target != 0x0000 && target != 0xffff then
          setEolComment(program, vec.toLong, f"Vector target = 0x$target%04X ($name)")
        else
          setEolComment(program, vec.toLong, f"Vector target = 0x$target%04X")

  private def annotateKnownRegions(program: Program): Unit =
    val tx = program.startTransaction("Annotate RL78/G12 regions")
    try
      ensureRegionLabel(program, 0x0000, "VECTOR_TABLE", "Vector table area")
      ensureRegionLabel(program, 0x0080, "CALLT_TABLE", "CALLT table area")
      ensureRegionLabel(program, 0x00C0, "OPTION_BYTES", "Option byte area")
      ensureRegionLabel(program, 0x00C4, "SECURITY_ID", "On-chip debug security ID setting area")

      // Typed bytes/words for well-known ROM regions
      for ((vec, _) <- vectors) do ensureWordData(program, vec.toLong)
      for off <- 0x00C0 to 0x00C3 do ensureByteData(program, off.toLong)
      for off <- 0x00C4 to 0x00CD do ensureByteData(program, off.toLong)

      annotateVectorEntries(program)

      Msg.showInfo(this, null, "RL78G12", "Known regions annotated.")
    catch
      case e: Exception =>
        Msg.showError(this, null, "RL78G12", s"Failed to annotate known regions: ${e.getMessage}", e)
    finally
      program.endTransaction(tx, true)

  private def renameInterrupts(program: Program): Unit =
    val tx = program.startTransaction("Label RL78 interrupt targets")
    try
      val mem = program.getMemory
      val st  = program.getSymbolTable

      for ((vec, name) <- vectors) do
        val vecAddr = addr(program, vec.toLong)

        createOrRenameLabel(program, vec.toLong, s"${name}_ADDR")

        if mem.contains(vecAddr) && mem.contains(vecAddr.add(1)) then
          val lo = mem.getByte(vecAddr) & 0xff
          val hi = mem.getByte(vecAddr.add(1)) & 0xff
          val target = (hi << 8) | lo

          if target != 0x0000 && target != 0xffff then
            val targetAddr = addr(program, target.toLong)

            ensureFunction(program, targetAddr, name)

            val syms = st.getSymbols(targetAddr)
            if syms.isEmpty then
              st.createLabel(targetAddr, name, SourceType.ANALYSIS)
            else
              val primary = st.getPrimarySymbol(targetAddr)
              if primary != null then
                try
                  primary.setName(name, SourceType.ANALYSIS)
                catch
                  case _: Exception =>
                    st.createLabel(targetAddr, f"${name}_${target}%04X", SourceType.ANALYSIS)

            setEolComment(program, vec.toLong, f"target -> $name @ 0x$target%04X")
            setPreComment(program, target.toLong, s"Interrupt handler for $name")

      Msg.showInfo(this, null, "RL78G12", "Interrupt labels added.")
    catch
      case e: Exception =>
        Msg.showError(this, null, "RL78G12", s"Failed to label interrupts: ${e.getMessage}", e)
    finally
      program.endTransaction(tx, true)

  private def renameDecompilerTemps(program: Program): Unit =
    val tx = program.startTransaction("Rename decompiler temps")
    val ifc = new DecompInterface()

    try
      if !ifc.openProgram(program) then
        Msg.showError(this, null, "RL78G12", "Failed to open program in decompiler interface.")
        return

      val fm = program.getFunctionManager
      val it = fm.getFunctions(true)

      var funcsVisited = 0
      var localsRenamed = 0

      while it.hasNext do
        val func = it.next()
        funcsVisited += 1

        val res = ifc.decompileFunction(func, 30, TaskMonitor.DUMMY)

        if res == null then
          Msg.warn(this, s"${func.getName}: decompile returned null")
        else if !res.decompileCompleted() then
          Msg.warn(this, s"${func.getName}: decompile failed: ${res.getErrorMessage}")
        else
          val hf = res.getHighFunction
          if hf == null then
            Msg.warn(this, s"${func.getName}: high function is null")
          else
            HighFunctionDBUtil.commitLocalNamesToDatabase(hf, SourceType.ANALYSIS)

            val locals = func.getLocalVariables
            var localIdx = 1

            for local <- locals do
              val oldName = local.getName
              if isTempName(oldName) then
                val newName = nextUniqueLocalName(func, localIdx)
                localIdx += 1

                try
                  local.setName(newName, SourceType.ANALYSIS)
                  localsRenamed += 1
                catch
                  case _: InvalidInputException =>
                    val fallback = nextUniqueNameFromBase(func, s"${newName}_")
                    try
                      local.setName(fallback, SourceType.ANALYSIS)
                      localsRenamed += 1
                    catch
                      case e: Exception =>
                        Msg.warn(this, s"${func.getName}: failed to rename local $oldName: ${e.getMessage}")
                  case e: Exception =>
                    Msg.warn(this, s"${func.getName}: failed to rename local $oldName: ${e.getMessage}")

        ifc.flushCache()

      Msg.showInfo(
        this,
        null,
        "RL78G12",
        s"Decompiler-temp pass complete. Functions visited: $funcsVisited, locals renamed: $localsRenamed"
      )
    catch
      case e: Exception =>
        Msg.showError(this, null, "RL78G12", s"Failed to rename decompiler temps: ${e.getMessage}", e)
    finally
      ifc.dispose()
      program.endTransaction(tx, true)

  private def isTempName(name: String): Boolean =
    tempVarRe.matches(name)

  private def nextUniqueLocalName(func: Function, startIdx: Int): String =
    var idx = startIdx
    var candidate = s"local_$idx"
    while hasVariableNamed(func, candidate) do
      idx += 1
      candidate = s"local_$idx"
    candidate

  private def nextUniqueNameFromBase(func: Function, base: String): String =
    var idx = 1
    var candidate = s"${base}$idx"
    while hasVariableNamed(func, candidate) do
      idx += 1
      candidate = s"${base}$idx"
    candidate

  private def hasVariableNamed(func: Function, name: String): Boolean =
    func.getAllVariables.exists(_.getName == name)
}
