  private def hasSymbolNamed(program: Program, addrValue: Long, name: String): Boolean =
    val st = program.getSymbolTable
    val syms = st.getSymbols(addr(program, addrValue))
    syms.exists(_.getName == name)

  private def createLabelAlias(program: Program, addrValue: Long, name: String, makePrimary: Boolean): Unit =
    val st = program.getSymbolTable
    val a = addr(program, addrValue)

    if !hasSymbolNamed(program, addrValue, name) then
      val sym = st.createLabel(a, name, SourceType.ANALYSIS)
      if makePrimary && sym != null then
        try sym.setPrimary()
        catch case _: Exception => ()

